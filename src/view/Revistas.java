    private void bRevistasConfirmarEdicaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRevistasConfirmarEdicaoActionPerformed
         try {
            RevistaControl revistaControl = new RevistaControl();
            DateFormat fmt = new SimpleDateFormat("dd/MM/yyyy");
            String dataRevista = fmt.format(this.calendarRevistasData.getDate());
            revistaControl.editar(this.txtRevistaID.getText(),this.txtRevistasTitulo.getText(), dataRevista,this.txtRevistasQuantidade.getText());
            this.buscaRevistaPrincipal.buscarRevistas();
            
            
            JOptionPane.showMessageDialog(rootPane, "Edição realizada com sucesso!", "Editar Revista", JOptionPane.INFORMATION_MESSAGE);
            
            this.dispose();
            
        } catch (ValidacaoException validacaoException) {
            
            JOptionPane.showMessageDialog(rootPane, validacaoException.getMessage(), "Editar Revista", JOptionPane.WARNING_MESSAGE);
        
        } catch (Exception ex) {
            
            JOptionPane.showMessageDialog(rootPane, "Erro ao efetuar a edição!", "Editar Revista", JOptionPane.ERROR_MESSAGE);
        
        }
    }//GEN-LAST:event_bRevistasConfirmarEdicaoActionPerformed

