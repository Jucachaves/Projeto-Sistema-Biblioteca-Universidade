    private void bRevistasCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRevistasCadastrarActionPerformed
        try {
            
            RevistaControl revistaControl = new RevistaControl();
            DateFormat fmt = new SimpleDateFormat("dd/MM/yyyy");
            String dataRevista = fmt.format(this.calendarRevistasData.getDate());
            revistaControl.cadastrar(this.txtRevistaID.getText(),this.txtRevistasTitulo.getText(), dataRevista, this.txtRevistasQuantidade.getText());
            this.buscaRevistaPrincipal.buscarRevistas();
            
            JOptionPane.showMessageDialog(rootPane, "Cadastro realizado com sucesso!", "Cadastro Revista", JOptionPane.INFORMATION_MESSAGE);
            
            this.dispose();
            
        } catch (ValidacaoException validacaoException) {
            
            JOptionPane.showMessageDialog(rootPane, validacaoException.getMessage(), "Cadastro Revista", JOptionPane.WARNING_MESSAGE);
        
        } catch (Exception ex) {
            
            JOptionPane.showMessageDialog(rootPane, "Erro ao efetuar o cadastro!", "Cadastro Revista", JOptionPane.ERROR_MESSAGE);
        
        }
    }//GEN-LAST:event_bRevistasCadastrarActionPerformed

